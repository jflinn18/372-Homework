package Gerbil;
import java.util.*;

public class HoppingGerbils {

	public static void main(String[] args)
	{
		List gerbils = new ArrayList();
		
		for (int i = 0; i < 25; i++)
			gerbils.add(new Gerbil(i));
		
		for (int i = 0; i < 25; i++)
		{
			System.out.printf("This is gerbil #%d", gerbils.get(i));
			Gerbil.hop(gerbils.get(i));
		}
		
		
	}
}
